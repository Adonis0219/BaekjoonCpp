#include<stdio.h>

int main() {
	int N;

	scanf_s("%d", &N);

	for (int i = 0; i < N; i++)
	{
		for (int j = 0; j < N - (i + 1); j++)
		{
			printf(" ");
		}

		for (int j = 0; j < i + 1; j++)
		{
			printf("*");
		}
		printf("\n");
	}
}

#include<iostream>

using namespace std;

int main() {
	int N;

	cin >> N;

	for (int i = 1; i <= N; i++)
	{
		for (int j = N; j > i; j--)
		{
			cout << " ";
		}
		
		for (int j = 0; j < i; j++)
		{
			cout << "*";
		}
		cout << '\n';
	}
}